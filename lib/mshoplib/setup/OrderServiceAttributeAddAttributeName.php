/**
 * Adds name column to order base service attribute table.
 */
class MW_Setup_Task_OrderServiceAttributeAddAttributeName extends MW_Setup_Task_Base
{
	private $mysql = array(
		'ALTER TABLE "mshop_order_base_service_attr" ADD "name" VARCHAR(255) NOT NULL AFTER "value"',
	);

	/**
	 * Returns the list of task names which this task depends on.
	 *
	 * @return string[] List of task names
	 */
	public function getPreDependencies()
	{
		return array( 'OrderRenameTables' );
	}


	/**
	 * Returns the list of task names which depends on this task.
	 *
	 * @return string[] List of task names
	 */
	public function getPostDependencies()
	{
		return array( 'TablesCreateMShop' );
	}


	/**
	 * Executes the task for MySQL databases.
	 */
	protected function mysql()
	{
		$this->process( $this->mysql );
	}


	/**
	 * Migrates service attribute data to list table.
	 *
	 * @param array $stmts Associative array of tables names and lists of SQL statements to execute.
	 */
	protected function process( array $stmts )
	{
		$this->msg( 'Adding name to order service attribute table', 0 );

		if( $this->schema->tableExists( 'mshop_order_base_service_attr' ) === true
			&& $this->schema->columnExists( 'mshop_order_base_service_attr', 'name' ) === false )
		{
			$this->executeList( $stmts );
			$this->status( 'added' );
		}
		else
		{
			$this->status( 'OK' );
		}
	}
}
